package barbero;

public class Silla {
boolean ocupada = false;
int id;

public Silla() {
	
}




public Silla(int id) {
	super();
	this.id = id;
}




public boolean isOcupada() {
	return ocupada;
}




public void setOcupada(boolean ocupada) {
	this.ocupada = ocupada;
}




public int getId() {
	return id;
}




public void setId(int id) {
	this.id = id;
}






}
